export function searchTemplates(query: string): WorkflowTemplate[] {
  const q = query.toLowerCase();
  return WORKFLOW_TEMPLATES.filter(t =>
    t.name.toLowerCase().includes(q) ||
    t.description.toLowerCase().includes(q) ||
    t.businessOutcome.toLowerCase().includes(q) ||
    t.category.includes(q) ||
    t.industryTags.some(tag => tag.includes(q)) ||
    t.channelMix.some(ch => ch.includes(q)) ||
    t.trigger.includes(q)
  );
}

export function getTemplatesByCategory(category: TemplateCategory): WorkflowTemplate[] {
  return WORKFLOW_TEMPLATES.filter(t => t.category === category);
}

export function getTemplateById(id: string): WorkflowTemplate | undefined {
  return WORKFLOW_TEMPLATES.find(t => t.id === id);
}

